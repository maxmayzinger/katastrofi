# Обработчик команды /start
@bot.message_handler(commands=['start'])
def start_command(message):
    bot.send_message(message.chat.id, "Привет! Я бот, который может дать тебе советы по экологии.  Напиши /eco, чтобы получить 7 случайных советов.")

# Обработчик команды /eco
@bot.message_handler(commands=['eco'])
def eco_command(message):
    # Выбираем 7 случайных советов
    random_tips = random.sample(eco_tips, 7)
    
    # Формируем текст сообщения
    response = "Вот 7 советов по экологии:\n\n"
    for i, tip in enumerate(random_tips):
        response += f"{i+1}. {tip}\n"

    # Отправляем сообщение пользователю
    bot.send_message(message.chat.id, response)

# Обработчик для любых других сообщений
@bot.message_handler(func=lambda message: True)
def echo_all(message):
    bot.reply_to(message, "Я понимаю только команды /start и /eco. Напиши /start для приветствия или /eco чтобы получить советы по экологии.")

# Запускаем бота
if __name__ == '__main__':
    bot.infinity_polling()
